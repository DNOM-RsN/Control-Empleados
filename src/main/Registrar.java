    private void BotonRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonRegistrarMouseClicked
        // TODO add your handling code here:
        String nombre = RegistrarNombre.getText();
        String edad = RegistrarEdad.getText();
        String dni = RegistrarDNI.getText();
        String tipo = RegistrarTipo.getText();
        String fecha = RegistrarFecha.getText();
        String salario = RegistrarSalario.getText();
        if (!nombre.isEmpty() && !edad.isEmpty() && !dni.isEmpty() && !tipo.isEmpty() && !fecha.isEmpty() && !salario.isEmpty()) {
        // Verificar si se ha seleccionado un cargo válido
            if (!cargoSeleccionado.equals("Seleccionar cargo")&& !busquedaCamposPredert()) {
                Empleados empleado = new Empleados(nombre, edad, dni, tipo, fecha, salario, cargoSeleccionado);
                //listaDatos.add(empleado);
                Datos.agregarEmpleado(empleado); 
                mostrarDialogoConfirmacion(empleado);
                limpiarCampos();
            } else {
                // Si el campo de cargo no ha sido seleccionado, mostrar un mensaje de error
                mostrarDialogoError();
            }
        } else {
            // Si algún campo está vacío, muestra un mensaje de error
            mostrarDialogoError();
        }
    }//GEN-LAST:event_BotonRegistrarMouseClicked

