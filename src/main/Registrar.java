    // Método para restablecer los campos de entrada
    private void restablecer() {
        String nombrePlaceholder = "Ingresar Nombre";
        String edadPlaceholder = "Ingresar Edad";
        String dniPlaceholder = "Ingresar DNI";
        String tipoPlaceholder = "Ingresar Tipo";
        String fechaPlaceholder = "Ingresar Fecha de Nacimiento";
        String salarioPlaceholder = "Ingresar Salario";

        if (RegistrarNombre.getText().isEmpty()) {
            RegistrarNombre.setText(nombrePlaceholder);
            RegistrarNombre.setForeground(new Color(153, 153, 153));
        }

        if (RegistrarEdad.getText().isEmpty()) {
            RegistrarEdad.setText(edadPlaceholder);
            RegistrarEdad.setForeground(new Color(153, 153, 153));
        }

        if (RegistrarDNI.getText().isEmpty()) {
            RegistrarDNI.setText(dniPlaceholder);
            RegistrarDNI.setForeground(new Color(153, 153, 153));
        }

        if (RegistrarTipo.getText().isEmpty()) {
            RegistrarTipo.setText(tipoPlaceholder);
            RegistrarTipo.setForeground(new Color(153, 153, 153));
        }

        if (RegistrarFecha.getText().isEmpty()) {
            RegistrarFecha.setText(fechaPlaceholder);
            RegistrarFecha.setForeground(new Color(153, 153, 153));
        }

        if (RegistrarSalario.getText().isEmpty()) {
            RegistrarSalario.setText(salarioPlaceholder);
            RegistrarSalario.setForeground(new Color(153, 153, 153));
        }
    }
    // Método para limpiar los campos de entrada
